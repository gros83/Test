package com.apoyoaula.reports.model;

public class GrupoInfo {
	private String folio;
	private String cct;
	private String turno;
	private String cod_nivel;
	private String grupo;
	private String asignatura;
	private String entidad;
	private String nom_esc;
	
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public String getCct() {
		return cct;
	}
	public void setCct(String cct) {
		this.cct = cct;
	}
	public String getTurno() {
		return turno;
	}
	public void setTurno(String turno) {
		this.turno = turno;
	}
	public String getCod_nivel() {
		return cod_nivel;
	}
	public void setCod_nivel(String cod_nivel) {
		this.cod_nivel = cod_nivel;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getAsignatura() {
		return asignatura;
	}
	public void setAsignatura(String asignatura) {
		this.asignatura = asignatura;
	}
	public String getEntidad() {
		return entidad;
	}
	public void setEntidad(String entidad) {
		this.entidad = entidad;
	}
	public String getNom_esc() {
		return nom_esc;
	}
	public void setNom_esc(String nom_esc) {
		this.nom_esc = nom_esc;
	}
	
}
